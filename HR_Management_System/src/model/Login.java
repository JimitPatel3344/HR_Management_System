package model;

public class Login {

		private int logid;
		private int eid;
		private String password;
		private int rid;
		public int getLogid() {
			return logid;
		}
		public void setLogid(Integer logid) {
			this.logid = logid;
		}
		public int getEid() {
			return eid;
		}
		public void setEid(Integer eid) {
			this.eid = eid;
		}
		public String getPassword() {
			return password;
		}
		public void setPassword(String password) {
			this.password = password;
		}
		public int getRid() {
			return rid;
		}
		public void setRid(Integer rid) {
			this.rid = rid;
		}
		
}
